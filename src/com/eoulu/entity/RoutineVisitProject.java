package com.eoulu.entity;

public class RoutineVisitProject {

	private int ID;
	private int Evolve;
	private int EvolveAnother;
	private int VisitID;
	private String Project;
	private String CheckSituation;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getEvolve() {
		return Evolve;
	}
	public void setEvolve(int evolve) {
		Evolve = evolve;
	}
	public int getEvolveAnother() {
		return EvolveAnother;
	}
	public void setEvolveAnother(int evolveAnother) {
		EvolveAnother = evolveAnother;
	}
	public int getVisitID() {
		return VisitID;
	}
	public void setVisitID(int visitID) {
		VisitID = visitID;
	}
	public String getProject() {
		return Project;
	}
	public void setProject(String project) {
		Project = project;
	}
	public String getCheckSituation() {
		return CheckSituation;
	}
	public void setCheckSituation(String checkSituation) {
		CheckSituation = checkSituation;
	}
	
	
	
}
