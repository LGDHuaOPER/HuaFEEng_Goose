package com.eoulu.entity;

public class ProductDrawings {
	
	private int ID;
	private String Category;
	private String ProductName;
	private String ProductType;
	private String Machine;
	private String VersionNO;
	private String DesignTime;
	private String Usage;
	private String CADdrawings;
	private String PDFdrawings;
	private String Remark;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getCategory() {
		return Category;
	}
	public void setCategory(String category) {
		Category = category;
	}
	public String getProductName() {
		return ProductName;
	}
	public void setProductName(String productName) {
		ProductName = productName;
	}
	public String getProductType() {
		return ProductType;
	}
	public void setProductType(String productType) {
		ProductType = productType;
	}
	public String getMachine() {
		return Machine;
	}
	public void setMachine(String machine) {
		Machine = machine;
	}
	public String getVersionNO() {
		return VersionNO;
	}
	public void setVersionNO(String versionNO) {
		VersionNO = versionNO;
	}
	public String getDesignTime() {
		return DesignTime;
	}
	public void setDesignTime(String designTime) {
		DesignTime = designTime;
	}
	public String getUsage() {
		return Usage;
	}
	public void setUsage(String usage) {
		Usage = usage;
	}
	public String getCADdrawings() {
		return CADdrawings;
	}
	public void setCADdrawings(String cADdrawings) {
		CADdrawings = cADdrawings;
	}
	public String getPDFdrawings() {
		return PDFdrawings;
	}
	public void setPDFdrawings(String pDFdrawings) {
		PDFdrawings = pDFdrawings;
	}
	public String getRemark() {
		return Remark;
	}
	public void setRemark(String remark) {
		Remark = remark;
	}
	
	

}
