package com.eoulu.entity;

public class OriginalQuotation {
	private int ID;
	private int CustomerID;
	private String Model;
	private String ChuckType;
	private String TemperatureRequirement;
	private String Microscope;
	private String VersionNumber;
	private String QuoteTotal;
	private String FilePath;
	private String Remarks;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getCustomerID() {
		return CustomerID;
	}
	public void setCustomerID(int customerID) {
		CustomerID = customerID;
	}
	public String getModel() {
		return Model;
	}
	public void setModel(String model) {
		Model = model;
	}
	public String getChuckType() {
		return ChuckType;
	}
	public void setChuckType(String chuckType) {
		ChuckType = chuckType;
	}
	public String getTemperatureRequirement() {
		return TemperatureRequirement;
	}
	public void setTemperatureRequirement(String temperatureRequirement) {
		TemperatureRequirement = temperatureRequirement;
	}
	public String getMicroscope() {
		return Microscope;
	}
	public void setMicroscope(String microscope) {
		Microscope = microscope;
	}
	public String getVersionNumber() {
		return VersionNumber;
	}
	public void setVersionNumber(String versionNumber) {
		VersionNumber = versionNumber;
	}
	public String getQuoteTotal() {
		return QuoteTotal;
	}
	public void setQuoteTotal(String quoteTotal) {
		QuoteTotal = quoteTotal;
	}
	public String getFilePath() {
		return FilePath;
	}
	public void setFilePath(String filePath) {
		FilePath = filePath;
	}
	public String getRemarks() {
		return Remarks;
	}
	public void setRemarks(String remarks) {
		Remarks = remarks;
	}
	
	

}
