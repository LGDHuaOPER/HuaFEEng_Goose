package com.eoulu.entity;

public class StandardProduct {
	private int ID;
	private String Model;
	private String Title;
	private String Type;
	private String Machine;
	private String ProductInstructions;
	private String InstallInstructions;
	private String TestInstructions;
	private String CheckingReport;
	private String DocumentIntegrity;
	private String UpdateTime;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getModel() {
		return Model;
	}
	public void setModel(String model) {
		Model = model;
	}
	public String getTitle() {
		return Title;
	}
	public void setTitle(String title) {
		Title = title;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	public String getMachine() {
		return Machine;
	}
	public void setMachine(String machine) {
		Machine = machine;
	}
	public String getInstallInstructions() {
		return InstallInstructions;
	}
	public void setInstallInstructions(String installInstructions) {
		InstallInstructions = installInstructions;
	}
	public String getTestInstructions() {
		return TestInstructions;
	}
	public void setTestInstructions(String testInstructions) {
		TestInstructions = testInstructions;
	}
	public String getCheckingReport() {
		return CheckingReport;
	}
	public void setCheckingReport(String checkingReport) {
		CheckingReport = checkingReport;
	}
	public String getDocumentIntegrity() {
		return DocumentIntegrity;
	}
	public void setDocumentIntegrity(String documentIntegrity) {
		DocumentIntegrity = documentIntegrity;
	}
	public String getProductInstructions() {
		return ProductInstructions;
	}
	public void setProductInstructions(String productInstructions) {
		ProductInstructions = productInstructions;
	}
	public String getUpdateTime() {
		return UpdateTime;
	}
	public void setUpdateTime(String updateTime) {
		UpdateTime = updateTime;
	}
	
	

}
