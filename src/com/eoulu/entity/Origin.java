package com.eoulu.entity;

public class Origin {

	private int ID;
	private String ManufactoryName;
	private String ManufactoryAddress;
	private String Goods;
	private String ModelNumber;
	private String Quality;
	private String LCNO;
	private String ContractNO;
	private String PurchaseOrderNO;
	private String OriginCountry;
	private String Tel;
	private String OperatingTime;
	private String Date;
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getManufactoryName() {
		return ManufactoryName;
	}
	public void setManufactoryName(String manufactoryName) {
		ManufactoryName = manufactoryName;
	}
	public String getManufactoryAddress() {
		return ManufactoryAddress;
	}
	public void setManufactoryAddress(String manufactoryAddress) {
		ManufactoryAddress = manufactoryAddress;
	}

	public String getLCNO() {
		return LCNO;
	}
	public void setLCNO(String lCNO) {
		LCNO = lCNO;
	}
	public String getContractNO() {
		return ContractNO;
	}
	public void setContractNO(String contractNO) {
		ContractNO = contractNO;
	}
	public String getPurchaseOrderNO() {
		return PurchaseOrderNO;
	}
	public void setPurchaseOrderNO(String purchaseOrderNO) {
		PurchaseOrderNO = purchaseOrderNO;
	}
	public String getOriginCountry() {
		return OriginCountry;
	}
	public void setOriginCountry(String originCountry) {
		OriginCountry = originCountry;
	}
	public String getTel() {
		return Tel;
	}
	public void setTel(String tel) {
		Tel = tel;
	}
	public String getOperatingTime() {
		return OperatingTime;
	}
	public void setOperatingTime(String operatingTime) {
		OperatingTime = operatingTime;
	}
	public String getDate() {
		return Date;
	}
	public void setDate(String date) {
		Date = date;
	}
	public String getGoods() {
		return Goods;
	}
	public void setGoods(String goods) {
		Goods = goods;
	}
	public String getModelNumber() {
		return ModelNumber;
	}
	public void setModelNumber(String modelNumber) {
		ModelNumber = modelNumber;
	}
	public String getQuality() {
		return Quality;
	}
	public void setQuality(String quality) {
		Quality = quality;
	}
	
	
}
