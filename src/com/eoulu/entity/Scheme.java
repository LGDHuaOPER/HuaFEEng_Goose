package com.eoulu.entity;

public class Scheme {
	
	private int ID;
	private String ApplicationType;
	private String CustomerName;
	private String SchemeName;
	private String MachineModel;
	private String MakeTime;
	private String CADdrawings;
	private String PDFdrawings;
	private String Remark;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getApplicationType() {
		return ApplicationType;
	}
	public void setApplicationType(String applicationType) {
		ApplicationType = applicationType;
	}
	public String getCustomerName() {
		return CustomerName;
	}
	public void setCustomerName(String customerName) {
		CustomerName = customerName;
	}
	public String getSchemeName() {
		return SchemeName;
	}
	public void setSchemeName(String schemeName) {
		SchemeName = schemeName;
	}
	public String getMachineModel() {
		return MachineModel;
	}
	public void setMachineModel(String machineModel) {
		MachineModel = machineModel;
	}
	public String getMakeTime() {
		return MakeTime;
	}
	public void setMakeTime(String makeTime) {
		MakeTime = makeTime;
	}
	public String getCADdrawings() {
		return CADdrawings;
	}
	public void setCADdrawings(String cADdrawings) {
		CADdrawings = cADdrawings;
	}
	public String getPDFdrawings() {
		return PDFdrawings;
	}
	public void setPDFdrawings(String pDFdrawings) {
		PDFdrawings = pDFdrawings;
	}
	public String getRemark() {
		return Remark;
	}
	public void setRemark(String remark) {
		Remark = remark;
	}
	
	

}
