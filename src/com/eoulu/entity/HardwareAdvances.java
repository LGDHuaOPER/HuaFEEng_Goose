package com.eoulu.entity;

public class HardwareAdvances {

	private int ID;
	private String Customer;
	private String InstalledTime;
	private int Status;
	private String ResponsibleAndProcess;
	private String OperatingTime;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getCustomer() {
		return Customer;
	}
	public void setCustomer(String customer) {
		Customer = customer;
	}
	public String getInstalledTime() {
		return InstalledTime;
	}
	public void setInstalledTime(String installedTime) {
		InstalledTime = installedTime;
	}
	public int getStatus() {
		return Status;
	}
	public void setStatus(int status) {
		Status = status;
	}
	public String getResponsibleAndProcess() {
		return ResponsibleAndProcess;
	}
	public void setResponsibleAndProcess(String responsibleAndProcess) {
		ResponsibleAndProcess = responsibleAndProcess;
	}
	public String getOperatingTime() {
		return OperatingTime;
	}
	public void setOperatingTime(String operatingTime) {
		OperatingTime = operatingTime;
	}
	
}
