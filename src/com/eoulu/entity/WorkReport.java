package com.eoulu.entity;

public class WorkReport {
	
	private int ID;
	private String Department;
	private String Name;
	private String Morning;
	private String Comments;
	private String Afternoon;
	private String MorningPlan;
	private String AfternoonPlan;
	private String Introspection;
	private String OperationTime;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getDepartment() {
		return Department;
	}
	public void setDepartment(String department) {
		Department = department;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getMorning() {
		return Morning;
	}
	public void setMorning(String morning) {
		Morning = morning;
	}
	public String getComments() {
		return Comments;
	}
	public void setComments(String comments) {
		Comments = comments;
	}
	public String getAfternoon() {
		return Afternoon;
	}
	public void setAfternoon(String afternoon) {
		Afternoon = afternoon;
	}
	public String getMorningPlan() {
		return MorningPlan;
	}
	public void setMorningPlan(String morningPlan) {
		MorningPlan = morningPlan;
	}
	public String getAfternoonPlan() {
		return AfternoonPlan;
	}
	public void setAfternoonPlan(String afternoonPlan) {
		AfternoonPlan = afternoonPlan;
	}
	public String getOperationTime() {
		return OperationTime;
	}
	public void setOperationTime(String operationTime) {
		OperationTime = operationTime;
	}
	public String getIntrospection() {
		return Introspection;
	}
	public void setIntrospection(String introspection) {
		Introspection = introspection;
	}
	
	
	
	

}
